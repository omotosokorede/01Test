void setupSPI(void){
	//turn on the clocks
	RCC->AHB1ENR |= RCC_AHB1ENR_GPIOAEN;
	RCC-> APB2ENR |= RCC_APB2ENR_SPI1EN;

	//SET UP THE GPIO
	GPIOA->MODER &= ~(GPIO_MODER_MODER5 | GPIO_MODER_MODE7);
	GPIOA-> MODER |=(GPIO_MODER_MODER5_1 | GPIO_MODER_MODE7_1);

	//CONFIGURE THE SPI
	SPI1->CR1 = SPI_CR1_BIDIMODE | SPI_CR1_BIDIOE | SPI_CR1_SSM | (0X3UL<< SPI_CR1_BR_Pos) | SPI_CR1_MSTR | SPI_CR1_CPHA |SPI_CR1_CPOL ;
	SPI1->CR2 |=SPI_CR2_SSOE;
	SPI1->CR1 |= SPI_CR1_SPE; //ENABLE THE SPI

}
void spi_send(uint8_t data){

	SPI1->DR =data;
	while((SPI1->SR & SPI_SR_TXE) == 0);

}

void clearArray(void){
	int i;
	int j;
	for (i =0 ; i< LEDCOUNT ; i++){
		LED_ARRAY[i][GLOBAL]= 0xE0;
		for (j =0 ; j< 4 ; j++){
			LED_ARRAY[i][j]= 0x00;
		}

	}
}
void skSetLed(uint8_t index, uint8_t global, uint8_t red,uint8_t blue, uint8_t green){
	LED_ARRAY[index][GLOBAL] = (0xE0 | global);
	LED_ARRAY[index][RED] = red;
	LED_ARRAY[index][BLUE] = blue;
	LED_ARRAY[index][GREEN] = green;
}
void skUpdate(void){

	//send start frame
	uint8_t i, j;
	for(i = 0 ; i<4 ; i++){
		spi_send(0x00);
	}

	//led rams
	for(i=0; i<LEDCOUNT; i++){
		spi_send( LED_ARRAY[i][j]);
		for(j=0 ; j<4; j++){
			spi_send( LED_ARRAY[i][j]);
		}
	}

	//end frames
	for(i = 0 ; i<4 ; i++){
		spi_send(0xFF);
	}
}

